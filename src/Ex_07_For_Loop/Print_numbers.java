package Ex_07_For_Loop;

public class Print_numbers {
    static void main(String[] args) {

        for(int i=1;i<=10;i++){
            System.out.println("Number is : "+ i);
        }
            for(int i=10;i>=0;i--){
                System.out.println("Number is : "+ i);
        }
            for (int i=0;i<10;i++){
                System.out.println("Number is : "+ i);
            }
            for (int i=0;i<10;++i){
                System.out.println("Number is : "+ i);
            }
    }
}
